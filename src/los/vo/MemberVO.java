package los.vo;

public class MemberVO {
	private String mem_id;
	private String mem_mail;
	private String mem_pass;
	private String mem_gubun;
	private String mem_status;
	private String mem_reg_date;
	
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getMem_mail() {
		return mem_mail;
	}
	public void setMem_mail(String mem_mail) {
		this.mem_mail = mem_mail;
	}
	public String getMem_pass() {
		return mem_pass;
	}
	public void setMem_pass(String mem_pass) {
		this.mem_pass = mem_pass;
	}
	public String getMem_gubun() {
		return mem_gubun;
	}
	public void setMem_gubun(String mem_gubun) {
		this.mem_gubun = mem_gubun;
	}
	public String getMem_status() {
		return mem_status;
	}
	public void setMem_status(String mem_status) {
		this.mem_status = mem_status;
	}
	public String getMem_reg_date() {
		return mem_reg_date;
	}
	public void setMem_reg_date(String mem_reg_date) {
		this.mem_reg_date = mem_reg_date;
	}
	
}
