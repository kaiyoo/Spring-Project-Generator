// 입력값(아이디) 체크
function idChecked(id){
	if(/^[0-9a-zA-Z]{5,20}$/.test(id)) {
		return true;
	}
	else {
		return false;
	}
}

// 입력값(비밀번호) 체크
function pwdCheck(pwd){
	if(/^[0-9a-zA-Z]{5,20}$/.test(pwd))
		return true;
	else
		return false;
}

//입력값(메일) 체크
function mailCheck(mail) {
	if(/^[a-z0-9_]{5,20}@[a-z]+(\.[a-z]+){1,2}/.test(mail)) {
		return true;
	} else {
		return false;
	}
}

// 프로젝트 , 패키지 명 체크
function projectCheck(name){
	if(/^[a-zA-Z][a-zA-Z0-9]{3,20}$/.test(name))
		return true;
	else
		return false;
}